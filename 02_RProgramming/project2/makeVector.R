makeVector <- function(x = numeric()) {
#     setwd("C:\\Users\\lenovo\\Desktop\\Rlearning\\r programming\\project2")
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setmean <- function(mean) m <<- mean
        getmean <- function() m
        list(set = set, get = get,
             setmean = setmean,
             getmean = getmean)
}